There are a number of mailing lists available for those interested in
Topographica.  Click on each one for information about signing up to
the list, unsubscribing, or accessing the list archives.

<DL>
<P><DT><A HREF="https://lists.sourceforge.net/lists/listinfo/topographica-announce">topographica-announce</A></DT> 
<DD>Mailing list for announcing significant releases and other
important news. Should have very low traffic, and will not clog your
inbox, and is thus suitable for anyone interested in Topographica.

<P><DT><A HREF="http://lists.sourceforge.net/mailman/listinfo/topographica-developers">topographica-developers</A></DT>
<DD>Mailing list for members of the Topographica development team.
Relatively low traffic, except for occasional short bursts of
discussion.
</DD>

<P><DT><A HREF="http://lists.sourceforge.net/lists/listinfo/topographica-cvs">topographica-cvs</A></DT> 
<DD>Mailing list announcing changes made to any Topographica file in
the SVN repository.  Extremely high traffic (often several emails per
day), and likely to be of interest only for active developers.</DD>
</DL>


<DT><A href="som_retinotopy.html"><strong>SOM Retinotopic Map</strong></A></DT>
<DD>How to run a model that develops selectivity for position, mapping
the input space to the cortical space.  The model uses the abstract
SOM algorithm, focusing on the basic principles of self-organization
rather than modeling any particular biological system.

</dl>

These should be good starting points for seeing how Topographica works
and how to use a Topographica model.  If you develop any other
tutorials for your own models, we would love to add them here!
